// while and do-while loop

let i = 0;  // 1st step initialization

while(i<=10){  // 2nd step condition
    console.log('hello i am under the water ',i);
    i++;    // increment
}

// Do while 

let k = 0    // initialization
do{
    console.log(k)   // printing
    k++  // increment
}while(k<=10)   // condition

